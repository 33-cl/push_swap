#include "push_swap.h"

int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	i;
	int				r;

	r = 0;
	while ((s1[i] || s2[i]) && r == 0)
	{
		if (s1[i] != s2[i])
		{
			r = s1[i] - s2[i];
		}
		i++;
	}
	return (r);
}
